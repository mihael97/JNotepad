package hr.fer.zemris.java.hw11.jnotepadpp.actions;

/**
 * Enumeration contains supported types for case changing
 * 
 * @author ime
 *
 */
public enum CaseEnum {
	/**
	 * Type if we want all upper characters
	 */
	UPPER,
	/**
	 * Type if we want all lower characters
	 */
	LOWER,
	/**
	 * Type if we want all inverted characters
	 */
	INVERT;
}
